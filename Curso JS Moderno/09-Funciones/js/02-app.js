// Function Declaration

function sumar() {
    console.log(2 + 2);
}


// Function Expression
const sumar2 = function() {
    console.log(3 + 3);
}

// La diferencia entre estas es que la declaracion de una funcion
// quiere decir que la puede llamar antes de definirla, es decir,
// no puedo llamar una Function Expression antes de ser declarada