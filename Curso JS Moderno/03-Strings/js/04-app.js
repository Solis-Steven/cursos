const saludo = "            Hola, que tal?          ";

/* Eliminar todos los espacios en blancon
que tenga un string al incio*/
console.log(saludo.trimStart());

/* Eliminar todos los espacios en blancon
que tenga un string al final*/
console.log(saludo.trimEnd());

/* Eliminar todos los espacios en blancon
que tenga un string al incio y al final*/
console.log(saludo.trimStart().trimEnd());